# Задача 2. Автотест API Яндекса
# Проверим правильность работы Яндекс Диск REST API. Напишите тесты, проверяющие создание папки на Диске.
# Используя библиотеку requests, напишите unit-test на верный ответ и возможные отрицательные тесты на ответы с ошибкой.
#
# Пример положительных тестов:
#
# код ответа соответствует 200;
# результат создания папки — папка появилась в списке файлов.

# ·         setUp: Создает папку перед каждым тестом.
# ·         tearDown: Удаляет созданную папку после каждого теста, чтобы не нарушать состояние системы.
# ·         test_create_folder_success: Проверяет успешное создание папки и наличие её в списке файлов.
# ·         test_create_folder_already_exists: Проверяет ответ API при попытке создать папку, которая уже существует (должен быть 409).
# ·         test_create_folder_invalid_token: Проверяет ошибку авторизации при использовании недопустимого токена (должен быть 401).
# ·         test_create_folder_invalid_path: Проверяет создание папки с недопустимым путем (должен быть 400 или 403).
